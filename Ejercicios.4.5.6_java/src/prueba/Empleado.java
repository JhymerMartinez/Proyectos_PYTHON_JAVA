package prueba;

public class Empleado {

	private String Nombre;
	private Integer Peso;
	
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public Integer getPeso() {
		return Peso;
	}
	public void setPeso(Integer peso) {
		Peso = peso;
	}

	
}
